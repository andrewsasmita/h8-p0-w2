// Melakukan Looping menggunakan While

var num1= 0
var num2= 20

console.log('LOOPING PERTAMA');

while (num1<20) {
    num1=num1+2
    console.log(num1+' - I love coding');
}

console.log('LOOPING KEDUA');

while (num2>0) {
    console.log(num2+' - I will become fullstack developer')
    num2=num2-2
}