var a=word.substring(0,3);
var b=word.substring(4,14);
var c=word.substring(15,17);
var d=word.substring(18,20);
var e=word.substring(21,25);

console.log('First Word: '+a+', With length: '+a.length);
console.log('Second Word: '+b+', With length: '+b.length);
console.log('Third Word: '+c+', With length: '+c.length);
console.log('Fourth Word: '+d+', With length: '+d.length);
console.log('Fifth Word: '+e+', With length: '+e.length);