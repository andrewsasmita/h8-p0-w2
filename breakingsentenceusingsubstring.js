// Breaking Sentence using Substring

var word ='wow JavaScript is so cool'
var a=word.substring(0,3);
var b=word.substring(4,14);
var c=word.substring(15,17);
var d=word.substring(18,20);
var e=word.substring(21,25);

console.log('First Word: '+a);
console.log('Second Word: '+b);
console.log('Third Word: '+c);
console.log('Fourth Word: '+d);
console.log('Fifth Word: '+e);
